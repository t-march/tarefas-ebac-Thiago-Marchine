    private Boolean isCamposValidos(String ...campos) {
        for (String campo : campos) {
            if (campo == null || "".equals(campo)) {
                return false;
            }
        }
        return true;
    }
    
    private boolean isNumero(String ...campos){
        for(String campo : campos){
            if(!NumberUtils.isCreatable(campo)){
                return false;
            }
        } return true;
    }
    
    
    
    

